        System.out.printf( "    TestFrame version:      %s\n",  version.getDecodedVersion() );
        System.out.printf( "    Card Version:           %s\n",  Card.getDecodedVersion() );
        System.out.printf( "    Deck Version:           %s\n",  Deck.getDecodedVersion() );
